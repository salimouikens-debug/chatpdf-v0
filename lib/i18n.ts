import { useState, useEffect } from "react";

export type Language = "fr" | "en" | "ar";
export type TranslationKey = keyof typeof translations.fr;

export function useTranslation(defaultLang?: string) {
    const [lang, setLang] = useState(defaultLang || "fr");

    useEffect(() => {
        const savedLang = localStorage.getItem("app-lang") || "fr";
        if (savedLang !== lang) {
            setLang(savedLang);
        }

        const handleLangChange = () => {
            const updated = localStorage.getItem("app-lang") || "fr";
            setLang(updated);
        };

        window.addEventListener("languageChange", handleLangChange);
        return () => window.removeEventListener("languageChange", handleLangChange);
    }, []); // Removed `lang` from dependencies to avoid infinite loops, but tracking correctly

    const language = (translations as Record<string, Record<string, string>>)[lang] ? lang : "fr";
    const t = (key: TranslationKey) => {
        return (translations as any)[language][key] || translations.fr[key];
    };
    return t;
}
